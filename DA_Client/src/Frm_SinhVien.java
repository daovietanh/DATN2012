    private void jCKyThiItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jCKyThiItemStateChanged
        try {
            // TODO add your handling code here:
            if (evt.getItem() == null) {
                resetPanelExam();
                return;
            }
            if (jCKyThi.getSelectedIndex() == 0) {
                resetPanelExam();
                return;
            }
            Exam e = (Exam) jCKyThi.getSelectedItem();
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
            txtMonThiKT.setText(Cl_Client.c.getSubjectByID(e.getSubjectID()).getSubjectName());
            dateStart.setText(sdf.format(e.getDateStart().getTime()));
            dateEnd.setText(sdf.format(e.getDateEnd().getTime()));
            txtThoiGianKT.setText(e.getTimes() + "");
            txtConLaiKT.setText("Đang tính");
            // So cau hoi lay ra tu table kythi-cauhoi
            txtSocau.setText(e.getNumberOfTest() + "");
            lblLuotLam.setText(e.getNumberOfTest() + "");
            lblTongSoLuotLam.setText(Cl_Client.c.getTotalTestOfExam(e)+"");

        } catch (RemoteException ex) {
            Logger.getLogger(Frm_SinhVien.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jCKyThiItemStateChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (FrmThi.isOpen == true) {
            JOptionPane.showMessageDialog(null, "Đang luyện tập");
            return;
        }
        try {
            // TODO add your handling code here:
            isTest = false;
            if (jCbMonThi.getSelectedIndex() == 0 || jCbDoKho.getSelectedIndex() == 0 || jCbSoCau.getSelectedIndex() == 0) {
                return;
            }
            Subject monthi = (Subject) jCbMonThi.getSelectedItem();
            int socau = Integer.parseInt(jCbSoCau.getSelectedItem().toString());
            String thoigian = txtThoiGian.getText();
            LevelAll dokho = (LevelAll) jCbDoKho.getSelectedItem();
            // Lưu lại các thông tin
            Session.monthi = monthi;
            Session.socauhoi = socau;
            Session.thoigian = Integer.parseInt(thoigian);
            Session.dokho = dokho;

            // Ngay
            Session.ngaythi = Calendar.getInstance();
            Session.kythi = null;

            //
            if (socau > Cl_Client.c.getAllQuestionBySubject(monthi.getSubjecId()).size()) {
                Cl_Client.ShowError("Không đủ câu hỏi");
                return;
            }
            this.dispose();
            FrmThi a = new FrmThi();
            FrmThi.isOpen = true;
            a.setVisible(true);

        } catch (RemoteException ex) {
            Logger.getLogger(Frm_SinhVien.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

