    public void loadform() {
        try {
            Users user = Session.user;
            idUser = user.getUserID();
            // --------------- load table 
            // Lich su thi
            jTableLichSu.setModel(Cl_Client.c.getAllTrainToTable());
            // ------------------  Tab Thi  --------------------------
            String username = user.getUserName();
            //String id = Cl_Client.c.getIDUser(user);

            // Add mon thi
            jCbMonThi.removeAllItems();
            List<Subject> allSubject = Cl_Client.c.getAllSubject();
            jCbMonThi.addItem("Lựa Chọn");
            if (allSubject.size() > 0) {
                for (int i = 0; i < allSubject.size(); i++) {
                    jCbMonThi.addItem(allSubject.get(i));
                }
            }
            // Add do kho
            jCbDoKho.removeAllItems();
            jCbDoKho.addItem("Lựa Chọn");
            List<LevelAll> allLevel = Cl_Client.c.getAllLevel();
            if (allLevel.size() > 0) {
                for (int i = 0; i < allLevel.size(); i++) {
                    jCbDoKho.addItem(allLevel.get(i));
                }
            }
            // so cau
            jCbSoCau.removeAllItems();
            jCbSoCau.addItem("Lựa Chọn");
            jCbSoCau.addItem(1);
            for (int i = 2; i <= 20; i++) {
                jCbSoCau.addItem(i);
            }

            // Ky Thi dang dien ra
            List<Exam> currentExam = Cl_Client.c.getCurrentExam();
            jCKyThi.addItem("Lựa Chọn");
            if (!currentExam.isEmpty()) {
                for (int i = 0; i < currentExam.size(); i++) {
                    jCKyThi.addItem(currentExam.get(i));
                }
            }

            // ------------  Tab Thông Tin Tài Khoản  ------------------
            // Hien thị thông tin sinh viên lên
            if (user.getEnable()) {
                cbx.setSelected(true);
            } else {
                cbx.setSelected(false);
            }
            pass.setText(user.getPassword());
            // Thông tin cá nhân
            lbl.setText(username);
            txtHoTen.setText(user.getFullName());
            txtLastLogin.setText(user.getLastLoginDate());
            try {
                String dateString = user.getBirthday();
                String s[] = dateString.split("-");
                int year = Integer.parseInt(s[2])-1900;
                int month = Integer.parseInt(s[1])- 1;
                int date = Integer.parseInt(s[0]);
                Date d = new Date(year, month, date);
                jDate.setDate(d);
            } catch(Exception e){
                
            }
            txtDiaChi.setText(user.getAddress());
        } catch (RemoteException ex) {
            Logger.getLogger(Frm_SinhVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

