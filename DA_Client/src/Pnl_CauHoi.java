private void jTCauHoiUMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTCauHoiUMouseReleased
// TODO add your handling code here:
        Long id = Main.client.getIDTable(jTCauHoiU);
        if (!id.equals("") || id!= -1) {
            try {
                Question bean = Cl_Client.c.getQuestiontByID(id);
                txtTieuDe1.setText(bean.getQuestionTitle());
                jTpNoiDung1.setText(bean.getContent());
                int level = bean.getLevelID();
                if (level == 1)
                    jCbDoKho1.setSelectedIndex(0);
                else if (level == 2)
                    jCbDoKho1.setSelectedIndex(1);
                else jCbDoKho1.setSelectedIndex(2);
                
                Subject s = Cl_Client.c.getSubjectByID(bean.getSubjectID());
                jCbMonThi1.getModel().setSelectedItem(s);
                
                
                String answer = bean.getAnswer() ;
                
                buttonGroup1.clearSelection();
                if (answer.endsWith("A"))
                    jRdA1.setSelected(true);
                if (answer.endsWith("B"))
                    jRdB1.setSelected(true);
                if (answer.endsWith("C"))
                    jRdC1.setSelected(true);
                if (answer.endsWith("D"))
                    jRdD1.setSelected(true);
                
                jTpA1.setText(bean.getChooseA());
                jTpB1.setText(bean.getChooseB());
                jTpC1.setText(bean.getChooseC());
                jTpD1.setText(bean.getChooseD());
                dataImagesU = bean.getPicture();
                showImageU();
            } catch (Exception ex) {
            }
        }
}//GEN-LAST:event_jTCauHoiUMouseReleased

