private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            // TODO add your handling code here:
            if (jTpNoiDung.getText().equals("")) {
                Cl_Client.ShowError("Bạn chưa nhập nội dung câu hỏi");
                return;
            }
            if (jTpA.getText().equals("") || jTpB.getText().equals("") || jTpC.getText().equals("") || jTpD.getText().equals("")) {
                Cl_Client.ShowError("Bạn phải nhập đầy đủ đáp án");
            }
            String da = "";
            if (jRdA.isSelected()) {
                da = "A";
            }
            if (jRdB.isSelected()) {
                da = "B";
            }
            if (jRdC.isSelected()) {
                da = "C";
            }
            if (jRdD.isSelected()) {
                da = "D";
            }
            if (da.equals("")) {
                Cl_Client.ShowError("Bạn phải chọn 1 đáp án đúng");
                return;
            }

            
            String usernameCreate = User.username ;
            Long idUser = null ;
            try {
                Users u = Cl_Client.c.getUserByUserName(usernameCreate);
                idUser = u.getUserID();
            } catch (Exception e) {
                idUser = null ;
            }
            
            Date d = new Date();
                SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
                String dateCreate = sdf.format(d);
                
            String tieude = txtTieuDe.getText();
            byte[] image = getDataImages();
            Subject s = (Subject) jCbMonThi.getSelectedItem();
            Question bean = new Question(tieude, idUser , jCbDoKho.getSelectedIndex()+1 , s.getSubjecId() ,jTpNoiDung.getText(),da, jTpA.getText(), jTpB.getText(),
                    jTpC.getText(), jTpD.getText(), dateCreate, image );
            
            if (Cl_Client.c.insertQuestion(bean)){
                resetAll();
                loadform();
            }
            else {
                JOptionPane.showMessageDialog(null, "Loi");
            }
            

        } catch (RemoteException ex) {
            Logger.getLogger(FrmThemCauHoi.class.getName()).log(Level.SEVERE, null, ex);
        }
}//GEN-LAST:event_btnThemActionPerformed

